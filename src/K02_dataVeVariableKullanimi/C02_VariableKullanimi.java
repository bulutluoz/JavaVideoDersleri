package K02_dataVeVariableKullanimi;

public class C02_VariableKullanimi {

    public static void main(String[] args) {

        int sayi = 20;

        System.out.println("9.satirda sayinin degeri : " + sayi);
        // 9.satirda sayinin degeri : 20


        sayi = 30 ;

        System.out.println("15.satirda sayinin degeri : " + sayi);

        // 15.satirda sayinin degeri : 30




    }
}
